def summ(a):
    return(sum(a))
a=[1,1,1,1,1]
b=[2,2,2]
c=[1,2,3,4]
print('Сумма чисел массива a равна:',summ(a))
print('Сумма чисел массива b равна:',summ(b))
print('Сумма чисел массива c равна:',summ(c))
print('Среднее арифметическое чисел массива a равно:',summ(a)/len(a))
print('Среднее арифметическое чисел массива b равно:',summ(b)/len(b))
print('Среднее арифметическое чисел массива c равно:',summ(c)/len(c))
       
    
